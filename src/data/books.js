export function searchBooks(query, mode, tags, anatomy) {
  if (!query && !tags?.length) return [];

  let results = [...MOCK_BOOKS];

  if (mode === "fragment" || mode === "vibe") {
    if (query) {
      const q = query.toLowerCase();
      results = results.filter(book => {
        const searchSpace = [
          book.title, book.author, book.synopsis,
          book.opening, book.epigraph, book.dedication,
          ...book.tags, ...book.mood, ...book.genres
        ].join(" ").toLowerCase();

        if (anatomy && anatomy !== "not-sure") {
          const anatomyMap = {
            opening: book.opening,
            epigraph: book.epigraph,
            dedication: book.dedication,
            body: book.synopsis,
            epilogue: book.synopsis,
            acknowledgments: book.dedication,
            "chapter-header": book.title,
          };
          return (anatomyMap[anatomy] || searchSpace).toLowerCase().includes(q) ||
            searchSpace.includes(q);
        }

        return searchSpace.includes(q) ||
          book.matchReason.toLowerCase().includes(q);
      });
    }
  }

  if (mode === "mood" && tags?.length) {
    results = results.filter(book => {
      return tags.some(tag =>
        book.tags.some(t => t.toLowerCase().includes(tag.replace(/-/g, " "))) ||
        book.mood.some(m => m.toLowerCase().includes(tag.replace(/-/g, " ")))
      );
    });
  }

  if (mode === "vibe" && tags?.length) {
    results = results.filter(book => {
      return tags.some(tag =>
        book.tags.some(t => t.toLowerCase().includes(tag.replace(/-/g, " ")))
      );
    });
  }

  // Score and sort
  results = results.map(book => ({
    ...book,
    score: Math.random() * 0.3 + 0.7
  })).sort((a, b) => b.score - a.score);

  return results.slice(0, 6);
}
